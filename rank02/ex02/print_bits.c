#include <unistd.h>

void	ft_print_bits(unsigned char octet)
{
	int		i;
	char	res;

	i = 8;
	res = 0;
	while (i--)
	{
		res = (octet >> i & 1) + '0';
		write(1, &res, 1);
	}
}
